    public void listarTablaUsuario(){
        List<Usuario> usuarios = usuarioLogic.listaUsuarios();
        modelo = (DefaultTableModel) jTableUsuario.getModel();
        modelo.setRowCount(0);
        Object[] ob = new Object[5];
        for (int i = usuarios.size() - 1; i >= 0; i--) {
            ob[0] = usuarios.get(i).getDni();
            ob[1] = usuarios.get(i).getNombre();
            ob[2] = usuarios.get(i).getContraseña();
            ob[3] = usuarios.get(i).getRol();
            ob[4] = usuarios.get(i).getEs_usuario();
            
            modelo.addRow(ob);
        }
        jTableUsuario.setModel(modelo);
        JTableHeader header = jTableUsuario.getTableHeader();
        header.setOpaque(false);
    }

