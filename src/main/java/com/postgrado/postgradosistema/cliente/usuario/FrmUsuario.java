    private void jtxtBuscarPorNombreUsuarioKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxtBuscarPorNombreUsuarioKeyReleased
        String nombre = jtxtBuscarPorNombreUsuario.getText();
        if(!nombre.isEmpty()){
            List<Usuario> usuarios = usuarioLogic.buscarUsuarioPorNombre(nombre);
            DefaultTableModel modelo = (DefaultTableModel) jTableUsuario.getModel();
            modelo.setRowCount(0);
            Object[] ob = new Object[5];
            for (int i = usuarios.size()-1;i>=0; i--) {
                ob[0] = usuarios.get(i).getDni();
                ob[1] = usuarios.get(i).getNombre();
                ob[2] = usuarios.get(i).getContraseña();
                ob[3] = usuarios.get(i).getRol();
                ob[4] = usuarios.get(i).getEs_usuario();
                modelo.addRow(ob);
            }
        }else {
            listarTablaUsuario();
        }
    }//GEN-LAST:event_jtxtBuscarPorNombreUsuarioKeyReleased

