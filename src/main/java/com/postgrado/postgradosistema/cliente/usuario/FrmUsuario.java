    private void jbtnModificarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnModificarUsuarioActionPerformed
        int filaSeleccionada = jTableUsuario.getSelectedRow();

        if (filaSeleccionada != -1){
            if (FrmLogin1.tipoUsuario.equals("Administrador")){
                if (jTableUsuario.getValueAt(filaSeleccionada, 3).toString().equals("I")) {
                    JOptionPane.showMessageDialog(null, "No se puede modificar un Usuario inactiva");
                } else {
                    String dni = jTableUsuario.getValueAt(filaSeleccionada, 0).toString();
                    String nombre = jTableUsuario.getValueAt(filaSeleccionada, 1).toString();
                    String password = jTableUsuario.getValueAt(filaSeleccionada, 2).toString();
                    String rol = jTableUsuario.getValueAt(filaSeleccionada, 3).toString();
                    Usuario usuario = new Usuario();
                    usuario.setDni(Integer.parseInt(dni));
                    usuario.setNombre(nombre);
                    usuario.setContraseña(password);
                    usuario.setRol(rol);
                    FrmModificarUsuario frmModificarUsuario = new FrmModificarUsuario(this, true);
                    frmModificarUsuario.setDatos(dni, nombre, password, rol);
                    frmModificarUsuario.setVisible(true);
                }
            }else {
                JOptionPane.showMessageDialog(null, "No tiene permisos para modificar un usuario");
            }
        }else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una fila");
        }
    }//GEN-LAST:event_jbtnModificarUsuarioActionPerformed

    private void jbtnEliminarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnEliminarUsuarioActionPerformed
        int filaSeleccionada = jTableUsuario.getSelectedRow();
        if (filaSeleccionada != -1){
            if(FrmLogin1.tipoUsuario.equals("Administrador")){
                if(jTableUsuario.getValueAt(filaSeleccionada, 3).toString().equals("I")){
                    JOptionPane.showMessageDialog(null, "No se puede eliminar un usuario Inactivo");
                }else{
                    int dni = Integer.parseInt(jTableUsuario.getValueAt(filaSeleccionada, 0).toString());
                    int confirmacion = JOptionPane.showConfirmDialog(null, "Esta seguro de eliminar el usuario con DNI: "+dni+"?");
                    if (confirmacion == JOptionPane.YES_OPTION){
                        Usuario usuario = new Usuario();
                        usuario.setDni(dni);
                        if (usuarioLogic.eliminarUsuario(usuario)){
                            JOptionPane.showMessageDialog(null, "Usuario eliminado");
                            listarTablaUsuario();
                        }else{
                            JOptionPane.showMessageDialog(null, "Error al eliminar usuario");
                        }
                    }else {
                        JOptionPane.showMessageDialog(null, "No se eliminó el usuario");
                    }
                }
            }else {
                JOptionPane.showMessageDialog(null, "No tiene permisos para eliminar un usuario");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione una fila usuario");
        }

    }//GEN-LAST:event_jbtnEliminarUsuarioActionPerformed

