    private void jbtnModificarAreaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnModificarAreaActionPerformed
        String idTxt = jtxtIdArea.getText().trim();
        String nombre = jtxtNombreArea.getText().trim();

        if (!nombre.isEmpty()) {
            int id = Integer.parseInt(idTxt);
            Area area = new Area();
            area.setNombre(nombre);
            area.setId(id);
            if (areaLogic.modificarArea(area)) {
                JOptionPane.showMessageDialog(this, "Se modific√≥ correctamente");
                jtxtIdArea.setText("");
                jtxtNombreArea.setText("");
                this.dispose();
                this.setVisible(false);
                FrmArea a = new FrmArea();
                a.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Error, no se pudo modificar");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Ingrese un nombre");
        }
    }//GEN-LAST:event_jbtnModificarAreaActionPerformed

