    public void listarTablaAreas() {
        List<Area> areas = areaLogic.listaAreas();
        modelo = (DefaultTableModel) jTableAreas.getModel();
        modelo.setRowCount(0); // Limpiar filas existentes en la tabla
        Object[] ob = new Object[3];
        for (int i = areas.size() - 1; i >= 0; i--) {
            ob[0] = areas.get(i).getId();
            ob[1] = areas.get(i).getNombre();
            ob[2] = areas.get(i).getEs_area();
            modelo.addRow(ob);
        }
        jTableAreas.setModel(modelo);
        JTableHeader header = jTableAreas.getTableHeader();
        header.setOpaque(false);
    }


