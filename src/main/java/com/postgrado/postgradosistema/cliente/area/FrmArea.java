    private void jbtnModificarAreaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnModificarAreaActionPerformed
        int filaSeleccionada = jTableAreas.getSelectedRow();
        if (filaSeleccionada != -1) {
            //Si la la columna Estado es igual a "Inactivo" no se puede modificar
            if (jTableAreas.getValueAt(filaSeleccionada, 2).toString().equals("I")) {
                JOptionPane.showMessageDialog(null, "No se puede modificar un √°rea inactiva");
            } else {
                int id = Integer.parseInt(jTableAreas.getValueAt(filaSeleccionada, 0).toString());
                String nombre = jTableAreas.getValueAt(filaSeleccionada, 1).toString();

                FrmModificarArea modiarea = new FrmModificarArea(this, true);
                modiarea.setDatos(id, nombre);
                modiarea.setVisible(true);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una fila");
        }

    }//GEN-LAST:event_jbtnModificarAreaActionPerformed

